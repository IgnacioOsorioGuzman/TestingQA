    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

         //Si el empleado no es valido, muestra mensaje de error 
        if (!misDatos.validarUsuario(txtUsuario.getText(), new String(txtClave.getPassword()))) {
            JOptionPane.showMessageDialog(rootPane, "Usuario o Clave Incorrectos","Error",JOptionPane.ERROR_MESSAGE);
            txtUsuario.setText(""); //Limpiamos JtextField txtUsuario
            txtClave.setText(""); //Limpiamos JtextField txtClave
            txtUsuario.requestFocusInWindow(); //Le damos Foco al JtextField txtUsuario
            return; //Si el ingreso no es valido, rompemos la continuidad de codigo
        }
        frmPrincipal a = new frmPrincipal(); //Creamos el objeto del formulario frmPrincipal
        this.setVisible(false); //Ocultamos formulario Login
        a.setDatosPrincipal(misDatos); //Enviamos los datos
        a.setPerfil(misDatos.getPerfil(txtUsuario.getText())); //Enviamos Perfil
        a.setClave(new String(txtClave.getPassword())); // Enviamos la clave
        a.setUsuario(txtUsuario.getText()); //Enviamos el nombre de usuario
        a.setExtendedState(JFrame.MAXIMIZED_BOTH); //Extendemos el frmPrincipal
        a.setVisible(true); //Hacemos visible el formulario frmPrincipal

    }//GEN-LAST:event_btnAceptarActionPerformed

