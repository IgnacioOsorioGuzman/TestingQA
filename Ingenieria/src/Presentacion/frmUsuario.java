        private void llenarTabla() {

        String Titulos[] = {"Usuario", "Clave", " perfil","idusuario"};
        String registro[] = new String[4];
        miTabla = new DefaultTableModel(null, Titulos);

       String sql = "SELECT nombre_usu, clave_usu, perfil_usu, idusuario "
               + "FROM usuario "
               + "INNER JOIN persona ON persona.idpersona = usuario.idpersona "
               + "INNER JOIN grado ON grado.idGrado = persona.idGrado "
               + "INNER JOIN escalafon ON escalafon.idEscalafon = persona.idEscalafon "
               + "WHERE ibm_per = '"+IBM+"' ";
        ResultSet rs = misDatos.getConsulta(sql);
        try {
            while (rs.next()) {

                registro[0] = rs.getString("nombre_usu");
                registro[1] = rs.getString("clave_usu");
                registro[2] = rs.getString("perfil_usu");
                registro[3] = rs.getString("idusuario");
                

                miTabla.addRow(registro);
            }
            tblTabla.setModel(miTabla);

        } catch (SQLException ex) {
            Logger.getLogger(frmUsuario.class.getName()).log(Level.SEVERE, null, ex);

        }


    }
    
    
