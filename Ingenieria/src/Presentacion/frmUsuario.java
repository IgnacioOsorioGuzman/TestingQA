    private void eliminarUsuario(){
          int row = tblTabla.getSelectedRow();              
        if (row == -1) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione Fila");
            return;
        }
              
        else  {
            
            misDatos.borrarUsuario(idusuarioTabla);
        llenarTabla();
            
        }
    }
    
    
    
    private void obtenerFilaUsuario(){
        
          int row = tblTabla.getSelectedRow();              
        if (row == -1) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione Fila");
            return;
        }
              
        else  {
                    
            btnModificar.setEnabled(true);
            btnGuardar.setEnabled(false);
            txtNombre.setEnabled(false);
            txtClave.setEnabled(false);
            cmbPerfil.setEnabled(false);
            txtClave.requestFocusInWindow();
            
            String usuario = tblTabla.getValueAt(row, 0).toString().trim();
            String clave = tblTabla.getValueAt(row, 1).toString().trim();
            String perfil = tblTabla.getValueAt(row, 2).toString().trim();
            String idusuario = tblTabla.getValueAt(row, 3).toString().trim();
            
            
            txtNombre.setText(usuario);
            txtClave.setText(clave);
            cmbPerfil.setSelectedItem(perfil);
            idusuarioTabla = idusuario;
           
            
        }
        
    }
    
    
    
    private void mostrarNombrePersona() {
        
        String sql = "SELECT CONCAT(nombre_gra,' ',nombre_esc,' ',nombre_per,' ',apellido_per) AS 'persona' "
                + "FROM persona "
                + "INNER JOIN grado ON grado.idGrado = persona.idGrado "
                + "INNER JOIN escalafon ON escalafon.idEscalafon = persona.idEscalafon "
                + "WHERE ibm_per = '"+IBM+"'";
        
        ResultSet a = misDatos.getConsulta(sql);
         try {
             while (a.next()){
                 
                 String nombre = a.getString("persona");
                 lblNombrePersona.setText("Nombre: "+nombre);
                 
             }
         } catch (SQLException ex) {
             Logger.getLogger(frmUsuario.class.getName()).log(Level.SEVERE, null, ex);
         }
        
    }
    
    
    
    
