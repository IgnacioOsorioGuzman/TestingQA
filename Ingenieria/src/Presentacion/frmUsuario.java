    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        
        
        
         if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese Nombre de Usuario", "Error", JOptionPane.ERROR_MESSAGE);
            txtNombre.requestFocusInWindow();
            
            return;
        }
         
         
                  
         
             if (txtClave.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese Clave para el Usuario", "Error", JOptionPane.ERROR_MESSAGE);
            txtClave.requestFocusInWindow();
            
            return;
        }
             
                if (cmbPerfil.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione Pefil para el Usuario", "Error", JOptionPane.ERROR_MESSAGE);
            cmbPerfil.requestFocusInWindow();
            
            return;
        }
                
                
                
      
        String nombre = txtNombre.getText().trim();
        String clave = txtClave.getText().trim();
        String perfil = cmbPerfil.getSelectedItem().toString();
        int id = idpersona;

        Usuario miUsuario = new Usuario(nombre, clave, perfil, id);

        String msg;
        if (nuevo) {
            
             if (misDatos.existeUsuario(txtNombre.getText())) {
                JOptionPane.showMessageDialog(null, "El Nombre para el usuario Ya Existe", "Error", JOptionPane.ERROR_MESSAGE);
                txtNombre.requestFocusInWindow();
                return;
            }
            
            msg = misDatos.agregarUsuario(miUsuario);
            JOptionPane.showMessageDialog(rootPane, msg);
              btnNuevo.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnModificar.setEnabled(false);
            llenarTabla();

        } else {
            
                        
            msg = misDatos.modificarUsuario(miUsuario);
            JOptionPane.showMessageDialog(rootPane, msg);
            btnNuevo.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnModificar.setEnabled(false);
            llenarTabla();
            
        }
        
        llenarTabla();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

